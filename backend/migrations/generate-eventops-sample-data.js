function sqlString(val) {
    if (val === null || val === undefined) return "NULL";
    return "'" + String(val).replace(/\\/g, "\\\\").replace(/'/g, "\\'") + "'";
}

function sqlJson(val) {
    if (val === null || val === undefined) return "NULL";
    return sqlString(JSON.stringify(val));
}

function sqlNum(val) {
    return val === null || val === undefined ? "NULL" : Number(val);
}

